package rs.ac.bg.etf.pkiproject.model;

/**
 *
 * @author Nemanja
 */
public class Item {
    
    private String id;
    private Food food;
    private int quantity;
    private double price;
    
    public Item() {
        
    }

    public Item(String id, Food food, int quantity, double price) {
        this.id = id;
        this.food = food;
        this.quantity = quantity;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Food getFood() {
        return food;
    }

    public void setFood(Food food) {
        this.food = food;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
}
